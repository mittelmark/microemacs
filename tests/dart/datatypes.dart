void main () {
    // Strings
    
    String firstName = "John" ;
    print("Firstname is $firstName");
    // Integers
    int inum = 41;
    print("integer: $inum");
    // Doubles
    double dnum = 19.99;
    print("double: $dnum");
    // Booleans
    bool mbool = true;
    print("Boolean: $mbool");
    // Dynamic
    dynamic fullname = "Jeff Smith";
    print("Dynamic: $fullname");
}
